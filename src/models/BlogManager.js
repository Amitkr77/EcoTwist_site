import mongoose, { Schema } from "mongoose";

const BlogManagerSchema = new Schema({
    name: {
        type: String,
        required: [true, 'Name is required'],
        trim: true
    },
    email: {
        type: String,
        required: [true, 'Email is required'],
        unique: true,
        trim: true,
        lowercase: true,
        match: [/^\S+@\S+\.\S+$/, 'Please enter a valid email address'],
    },
    password: {
        type: String,
        required: [true, 'Password is required'],
        minlength: [8, 'Password must be at least 8 characters'],
    },
}, { timestamps: true })

BlogManager.index({ email: 1 })
export default mongoose.models.BlogManager || mongoose.model('BlogManager', BlogManagerSchema)